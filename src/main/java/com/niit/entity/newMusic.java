package com.niit.entity;

public class newMusic {
    private int mId;
    private String musicSrc;
    private String mTitle;
    private String mAlbum;
    private String mSinger;
    private String mLyrics;
    private String mCompose;

    public int getmId() {
        return mId;
    }

    public void setmId(int mId) {
        this.mId = mId;
    }

    public String getMusicSrc() {
        return musicSrc;
    }

    public void setMusicSrc(String musicSrc) {
        this.musicSrc = musicSrc;
    }

    public String getmTitle() {
        return mTitle;
    }

    public void setmTitle(String mTitle) {
        this.mTitle = mTitle;
    }

    public String getmAlbum() {
        return mAlbum;
    }

    public void setmAlbum(String mAlbum) {
        this.mAlbum = mAlbum;
    }

    public String getmSinger() {
        return mSinger;
    }

    public void setmSinger(String mSinger) {
        this.mSinger = mSinger;
    }

    public String getmLyrics() {
        return mLyrics;
    }

    public void setmLyrics(String mLyrics) {
        this.mLyrics = mLyrics;
    }

    public String getmCompose() {
        return mCompose;
    }

    public void setmCompose(String mCompose) {
        this.mCompose = mCompose;
    }
}
